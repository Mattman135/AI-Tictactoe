export default function translateIndex(i) {
  if (i == 0) {
    return [0, 0]
  }
  if (i == 1) {
    return [0, 1]
  }
  if (i == 2) {
    return [0, 2]
  }
  if (i == 3) {
    return [1, 0]
  }
  if (i == 4) {
    return [1, 1]
  }
  if (i == 5) {
    return [1, 2]
  }
  if (i == 6) {
    return [2, 0]
  }
  if (i == 7) {
    return [2, 1]
  }
  if (i == 8) {
    return [2, 2]
  }
}
